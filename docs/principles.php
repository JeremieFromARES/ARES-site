<p>
<span class="bigbig">ARES Principle/Philosophy</span><br>
<br>
<span class="big">Simplicity of writing,</span>
<br>
<span class="big">Simplicity of comprehension,</span>
<br>
<span class="big">No compromising,</span>
<br>
<span class="big">No quirks, no bs.</span>
<br><br><br><br>
<span class="bigbig">Simplicity of writing:</span><br>
Repeated keywords and data type names are short and abbreviated in a way that makes them easy to write.<br>
ARES breaks down the format of the code to its fundamentals, which leads to less time thinking and caring about details will negatively affect productivity and creativity.
<br><br>
<span class="bigbig">Simplicity of comprehension:</span><br>
Keywords and standard methods are explicitly named and act as the user thinks it should act.<br>
Multiple methods can lead to the same result, but this diversity of choice is meaningful in a dev's ability to be creative and productive.
<br><br>
<span class="bigbig">No compromising:</span><br>
Simplicity should not come at the cost of performances.<br>
What ARES does is to not reinvent the wheel: it takes from the bests to give the best back to its users.
<br><br>
<span class="bigbig">No quirks, no bs:</span><br>
ARES and its standard methods should act exactly like the user thinks it should act.<br>
Any quirk is another barrier to entry, barrier to learning, barrier to enjoying coding.<br>
Behavior should be standard across the whole medium.
<br><br>
Anything that is unnecessary should not be there in the first place.
</p>
